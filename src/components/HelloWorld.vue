import { ref, nextTick } from "vue";

export default {
  setup() {
    let total = ref(5);
    let items = ref([]);
    let time = ref("");
    for (var i = 0; i < total.value; i++) {
      items.value.push({
        id: i,
        label: String(Math.random()).slice(0, 5),
      });
    }
    function shuffle2() {
      // this.action = "shuffle";
      items.value = shuffle(items.value);
      let s = window.performance.now();
      nextTick(() => {
        time.value = window.performance.now() - s;
        console.log(time.value);
      });
    }
    return {
      total,
      items,
      shuffle2,
      time,
    };
  },
};
